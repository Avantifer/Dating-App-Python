export class Characteristic {
    img : string
    text : string

    constructor(img : string, text : string) {
        this.img = img
        this.text = text
    }
}

export class CharacteristicProduct {
    img : string
    title : string
    text : string

    constructor(img : string, title : string, text : string) {
        this.img = img
        this.title = title
        this.text = text
    }
}